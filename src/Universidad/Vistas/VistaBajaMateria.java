    private void armarCabecera() {
        ArrayList<Object> titulos = new ArrayList<>();

        titulos.add("ID");
        titulos.add("Materia");
        titulos.add("AÃ±o");
        titulos.add("Activo");

        for (Object titulo: titulos) {
            modelo.addColumn(titulo);
        }

        jTableBajaMateria.setModel(modelo);
    }
    
    private void llenarTablaTodos() {
        for (Materia materia: materias) {
            modelo.addRow(new Object[]{materia.getIdMateria(), materia.getNombre(), materia.getAnio(), materia.isActivo()});
        }
    }

